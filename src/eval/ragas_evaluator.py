"""
RAGAS Evaluation Pipeline for ContractIQ
==========================================
Automatically scores RAG pipeline quality using RAGAS metrics.

Metrics:
- Faithfulness: Does the answer stick to the retrieved context?
  (1.0 = fully grounded, 0.0 = hallucinating)
- Answer Relevancy: Does the answer actually address the question?
  (1.0 = perfectly relevant, 0.0 = completely off-topic)
- Context Precision: Are the retrieved chunks actually useful?
  (1.0 = all retrieved chunks relevant, 0.0 = all noise)
- Context Recall: Did we retrieve everything needed to answer?
  (1.0 = nothing missed, 0.0 = missed all relevant context)

Why this matters:
- Proves your RAG system actually works — not just built
- Real numbers in README signal senior engineering thinking
- Almost no fresher candidate in India has eval scores
- Directly answers "how do you know your RAG is good?"
"""

import os
import sys
import json
from typing import List, Dict, Optional, Any
from datetime import datetime
from pathlib import Path
from datasets import Dataset
from ragas import evaluate
from ragas.metrics import (
    faithfulness,
    answer_relevancy,
    context_precision,
    context_recall,
)
from langchain_core.documents import Document

from logger import GLOBAL_LOGGER as log
from exception.custom_exception import DocumentPortalException


class RAGASEvaluator:
    """
    Evaluates ContractIQ RAG pipeline quality using RAGAS framework.

    Usage:
        evaluator = RAGASEvaluator()

        # Add test cases
        evaluator.add_test_case(
            question="What is the notice period in this contract?",
            answer="The notice period is 30 days as stated in clause 8.",
            contexts=["Clause 8: Either party may terminate with 30 days written notice."],
            ground_truth="30 days notice period required for termination."
        )

        # Run evaluation
        scores = evaluator.evaluate()
        print(scores)
        # {
        #   "faithfulness": 0.87,
        #   "answer_relevancy": 0.91,
        #   "context_precision": 0.84,
        #   "context_recall": 0.79
        # }
    """

    def __init__(self, results_dir: str = "eval_results"):
        self.results_dir = Path(results_dir)
        self.results_dir.mkdir(parents=True, exist_ok=True)
        self.test_cases: List[Dict] = []
        log.info("RAGASEvaluator initialized", results_dir=str(self.results_dir))

    def add_test_case(
        self,
        question: str,
        answer: str,
        contexts: List[str],
        ground_truth: Optional[str] = None,
    ) -> None:
        """
        Add a single test case for evaluation.

        Args:
            question: The question asked about the contract
            answer: The answer generated by your RAG pipeline
            contexts: List of retrieved context chunks used to generate answer
            ground_truth: Optional expected answer (needed for context_recall)
        """
        self.test_cases.append({
            "question": question,
            "answer": answer,
            "contexts": contexts,
            "ground_truth": ground_truth or "",
        })
        log.info("Test case added", total=len(self.test_cases))

    def add_test_cases_bulk(self, cases: List[Dict]) -> None:
        """
        Add multiple test cases at once.

        Args:
            cases: List of dicts with keys: question, answer, contexts, ground_truth
        """
        for case in cases:
            self.add_test_case(
                question=case["question"],
                answer=case["answer"],
                contexts=case["contexts"],
                ground_truth=case.get("ground_truth", ""),
            )

    def evaluate(
        self,
        metrics: Optional[List] = None,
        save_results: bool = True,
    ) -> Dict[str, float]:
        """
        Run RAGAS evaluation on all added test cases.

        Args:
            metrics: List of RAGAS metrics to evaluate.
                     Defaults to [faithfulness, answer_relevancy,
                     context_precision, context_recall]
            save_results: Whether to save results to JSON file

        Returns:
            Dict of metric_name -> score (0.0 to 1.0)
        """
        try:
            if not self.test_cases:
                raise ValueError("No test cases added. Call add_test_case() first.")

            log.info("Starting RAGAS evaluation", test_cases=len(self.test_cases))

            # Default metrics
            if metrics is None:
                metrics = [
                    faithfulness,
                    answer_relevancy,
                    context_precision,
                    context_recall,
                ]

            # Build HuggingFace Dataset from test cases
            dataset = Dataset.from_dict({
                "question": [tc["question"] for tc in self.test_cases],
                "answer": [tc["answer"] for tc in self.test_cases],
                "contexts": [tc["contexts"] for tc in self.test_cases],
                "ground_truth": [tc["ground_truth"] for tc in self.test_cases],
            })

            # Run evaluation
            results = evaluate(dataset, metrics=metrics)

            # Extract scores
            scores = {
                "faithfulness": round(float(results["faithfulness"]), 4),
                "answer_relevancy": round(float(results["answer_relevancy"]), 4),
                "context_precision": round(float(results["context_precision"]), 4),
                "context_recall": round(float(results["context_recall"]), 4),
                "test_cases": len(self.test_cases),
                "evaluated_at": datetime.utcnow().isoformat(),
            }

            log.info("RAGAS evaluation complete", scores=scores)

            if save_results:
                self._save_results(scores)

            return scores

        except Exception as e:
            log.error("RAGAS evaluation failed", error=str(e))
            raise DocumentPortalException("RAGAS evaluation failed", e) from e

    def _save_results(self, scores: Dict) -> str:
        """Save evaluation results to JSON file."""
        timestamp = datetime.utcnow().strftime("%Y%m%d_%H%M%S")
        filename = self.results_dir / f"eval_{timestamp}.json"
        with open(filename, "w", encoding="utf-8") as f:
            json.dump(scores, f, indent=2)
        log.info("Evaluation results saved", file=str(filename))
        return str(filename)

    def load_latest_results(self) -> Optional[Dict]:
        """Load the most recent evaluation results."""
        files = sorted(self.results_dir.glob("eval_*.json"), reverse=True)
        if not files:
            return None
        with open(files[0], "r", encoding="utf-8") as f:
            return json.load(f)

    def clear_test_cases(self) -> None:
        """Clear all test cases for a fresh evaluation run."""
        self.test_cases = []
        log.info("Test cases cleared")


class ContractEvalSuite:
    """
    Pre-built evaluation suite for legal contract QnA.
    Contains standard test cases covering common contract questions.

    Use this to generate your README eval scores.
    """

    # Standard contract test cases — works with any NDA or employment contract
    STANDARD_TEST_CASES = [
        {
            "question": "What is the notice period for termination?",
            "ground_truth": "The notice period required for contract termination.",
        },
        {
            "question": "Who are the parties involved in this agreement?",
            "ground_truth": "The names and roles of all parties in the contract.",
        },
        {
            "question": "What are the confidentiality obligations?",
            "ground_truth": "The confidentiality requirements each party must follow.",
        },
        {
            "question": "What is the governing law for this contract?",
            "ground_truth": "The jurisdiction and governing law specified in the contract.",
        },
        {
            "question": "What are the payment terms?",
            "ground_truth": "The payment schedule, amounts, and conditions specified.",
        },
    ]

    @staticmethod
    def run_eval_with_rag(
        rag_instance,
        test_cases: Optional[List[Dict]] = None,
        save_results: bool = True,
    ) -> Dict[str, float]:
        """
        Run evaluation by passing test questions through your RAG pipeline.

        Args:
            rag_instance: Your ContractRAG instance (already built with docs)
            test_cases: Optional custom test cases. Uses STANDARD_TEST_CASES if None.
            save_results: Save results to eval_results/ directory

        Returns:
            Dict of RAGAS scores

        Example:
            rag = ContractRAG(session_id="eval_session")
            rag.build(docs, faiss_dir="faiss_index/eval")

            scores = ContractEvalSuite.run_eval_with_rag(rag)
            print(scores)
        """
        evaluator = RAGASEvaluator()
        cases = test_cases or ContractEvalSuite.STANDARD_TEST_CASES

        log.info("Running ContractIQ eval suite", cases=len(cases))

        for case in cases:
            try:
                # Run question through RAG pipeline
                result = rag_instance.invoke(
                    question=case["question"],
                    chat_history=[],
                )

                answer = result.get("answer", "")
                source_docs = result.get("source_documents", [])
                contexts = [doc["content"] for doc in source_docs]

                if not contexts:
                    log.warning("No contexts retrieved", question=case["question"])
                    continue

                evaluator.add_test_case(
                    question=case["question"],
                    answer=answer,
                    contexts=contexts,
                    ground_truth=case.get("ground_truth", ""),
                )

            except Exception as e:
                log.warning(
                    "Test case failed — skipping",
                    question=case["question"],
                    error=str(e),
                )
                continue

        if not evaluator.test_cases:
            raise ValueError("All test cases failed. Check your RAG pipeline.")

        return evaluator.evaluate(save_results=save_results)
